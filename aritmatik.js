function rumusLingkaran (phi,jari1, jari2){
    let hasil=phi * jari1 * jari2
    return hasil
}
let answer=rumusLingkaran(22/77,45,75)
console.log (answer)

function rumusSegiTiga (alas,tinggi, setengah){
    let luas=alas * tinggi * setengah
    return luas
}
let jawaban=rumusSegiTiga (1/2,52, 43)
console.log (jawaban)

function belanja (sepatu, tas, dompet, kalung, jamTangan){
    let harga=sepatu+tas+dompet+kalung+jamTangan
    return harga
}
